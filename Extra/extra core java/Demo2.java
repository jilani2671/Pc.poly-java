class MyThread extends Thread
{
	public void run()
	{
		for(int i=1;i<4;i++)
		{
			System.out.println(getName()+":"+i);
		try
		{
			sleep(1000);
		}
		catch(InterruptedException e)
		{
			System.out.println(e);
                                     }
}
	}
}

class Demo
{
	public static void main(String ar[])
	{
		MyThread m=new MyThread();
		m.setName("one");
		m.start();
	}
}


			






































































































































































































































































		